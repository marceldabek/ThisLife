// -------------------------------------------------------
// Utility: getRandomName
// -------------------------------------------------------
export function getRandomName(gender: 'male' | 'female'): {
  firstName: string;
  lastName: string;
} {
  const firstNames =
    gender === 'male' ? MALE_FIRST_NAMES : FEMALE_FIRST_NAMES;
  const firstName = firstNames[Math.floor(Math.random() * firstNames.length)];
  const lastName = LAST_NAMES[Math.floor(Math.random() * LAST_NAMES.length)];
  return { firstName, lastName };
}

// -------------------------------------------------------
// Utility: getRandomTraits
// -------------------------------------------------------
export function getRandomTraits(count: number): string[] {
  const pool = [...TRAIT_NAMES];
  const picked: string[] = [];
  const n = Math.min(count, pool.length);

  for (let i = 0; i < n; i++) {
    const idx = Math.floor(Math.random() * pool.length);
    picked.push(pool[idx]);
    pool.splice(idx, 1);
  }

  return picked;
}

// -------------------------------------------------------
// Utility: getEmoji
// -------------------------------------------------------
export function getEmoji(gender: 'male' | 'female', age: number): string {
  if (age <= 4) return '\u{1F476}';          // 👶
  if (age <= 12)
    return gender === 'male' ? '\u{1F466}' : '\u{1F467}'; // 👦 / 👧
  if (age <= 17)
    return gender === 'male' ? '\u{1F466}' : '\u{1F467}'; // 👦 / 👧
  if (age <= 59)
    return gender === 'male' ? '\u{1F468}' : '\u{1F469}'; // 👨 / 👩
  return gender === 'male' ? '\u{1F474}' : '\u{1F475}';   // 👴 / 👵
}
